#include "IERG3810_LED.h"
void IERG3810_LED_Init(void)
{
	RCC->APB2ENR |= 1<<3;
	RCC->APB2ENR |= 1<<6;
  GPIOB->CRL &= 0xFF0FFFFF;
  GPIOB->CRL |= 0x00300000;
  GPIOE->CRL &= 0xFF0FFFFF;
  GPIOE->CRL |= 0x00300000;
	
	//LED1
	GPIOE->CRL &= 0xFF0FFFFF;
	GPIOE->CRL |= 0x00300000;
	
	//LED0
	GPIOB->CRL &= 0xFF0FFFFF;
	GPIOB->CRL |= 0x00300000;
}
